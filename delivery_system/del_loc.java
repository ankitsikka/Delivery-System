    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        Connection myconnection;
        String path="jdbc:mysql://localhost/";
        String place="deliverysystemdb";
        String username="root";
        String password="";
        try
        {
            myconnection =DriverManager.getConnection(path+place,username,password);
            try
            {
                String q="select* from areatable where area=?";
                PreparedStatement ms =myconnection.prepareStatement(q);

                ms.setString(1, jTextField4.getText());
                ResultSet myresult=ms.executeQuery();
                if(myresult.next())
                {
                    jLabel11.setText(myresult.getString("shipcharges"));
                    jLabel7.setText(myresult.getString("countryname"));
                    jLabel8.setText(myresult.getString("statename"));
                    jLabel9.setText(myresult.getString("cityname"));
                     jTextField1.setText(myresult.getString("mincharges"));
                    
JOptionPane.showMessageDialog(rootPane,"Record showed successfully ");
                }

                
                else
                            {
                             JOptionPane.showMessageDialog(rootPane,"No Record Founds ");
                              jLabel7.setText("");
                jLabel8.setText("");
                jLabel9.setText("");
                
                jLabel11.setText("");
                jTextField4.setText("");
                jTextField1.setText("");
                            
                             }

            }

            catch(Exception e)
            {
                JOptionPane.showMessageDialog(rootPane,"Error in query "+e.getMessage());
            }
            finally
            {
                myconnection.close();
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane,"Error in query "+e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

